package Encapsultion;

public class Encapsulate {
	private int a;
	private int b;

	private int add(int d, int g) {
		return d + g;

	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

}
